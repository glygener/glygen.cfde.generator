package org.glygen.cfde.generator.om;

public class DCC
{
    private String m_id = null;
    private String m_name = null;
    private String m_abbr = null;
    private String m_description = null;
    private String m_email = null;
    private String m_contact = null;
    private String m_url = null;

    public String getId()
    {
        return this.m_id;
    }

    public void setId(String a_id)
    {
        this.m_id = a_id;
    }

    public String getName()
    {
        return this.m_name;
    }

    public void setName(String a_name)
    {
        this.m_name = a_name;
    }

    public String getAbbr()
    {
        return this.m_abbr;
    }

    public void setAbbr(String a_abbr)
    {
        this.m_abbr = a_abbr;
    }

    public String getDescription()
    {
        return this.m_description;
    }

    public void setDescription(String a_description)
    {
        this.m_description = a_description;
    }

    public String getEmail()
    {
        return this.m_email;
    }

    public void setEmail(String a_email)
    {
        this.m_email = a_email;
    }

    public String getContact()
    {
        return this.m_contact;
    }

    public void setContact(String a_contact)
    {
        this.m_contact = a_contact;
    }

    public String getUrl()
    {
        return this.m_url;
    }

    public void setUrl(String a_url)
    {
        this.m_url = a_url;
    }

}
